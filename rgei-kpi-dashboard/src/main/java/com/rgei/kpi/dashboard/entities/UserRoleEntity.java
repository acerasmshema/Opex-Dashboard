package com.rgei.kpi.dashboard.entities;

import java.io.Serializable;
import java.sql.Timestamp;

import javax.persistence.*;
import java.util.List;


@Entity
@Table(name="user_role")
public class UserRoleEntity implements Serializable {
	
	private static final long serialVersionUID = 1L;
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name="role_id")
	private Long roleId;

	private String description;

	@Column(name="role_name")
	private String roleName;
	
	@Column(name="created_date")
	private Timestamp createdDate;

	@Column(name="updated_date")
	private String updatedBy;

	@ManyToMany
	@JoinTable(
		name="rge_user_role"
		, joinColumns={
			@JoinColumn(name="role_id")
			}
		, inverseJoinColumns={
			@JoinColumn(name="user_id")
			}
		)
	private List<RgeUserEntity> rgeUsers;

	public Long getRoleId() {
		return this.roleId;
	}

	public void setRoleId(Long roleId) {
		this.roleId = roleId;
	}

	public String getDescription() {
		return this.description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getRoleName() {
		return this.roleName;
	}

	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	public List<RgeUserEntity> getRgeUsers() {
		return this.rgeUsers;
	}

	public void setRgeUsers(List<RgeUserEntity> rgeUsers) {
		this.rgeUsers = rgeUsers;
	}

	public Timestamp getCreatedDate() {
		return createdDate;
	}

	public void setCreatedDate(Timestamp createdDate) {
		this.createdDate = createdDate;
	}

	public String getUpdatedBy() {
		return updatedBy;
	}

	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}

}
